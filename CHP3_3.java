public class CHP3_3 {
    public static void main(String[] args) {
        //문자 자료형(활용성은 적지만 문잣값 표현하는 방식은
        //다양하므로 사용시 주의해야 함.
        char a1 ='a';//문잣값 앞뒤를''작은 따옴표로 감싸주어야 한다.
        char a2 =97;//아스키코드는 작은 따옴표로 감싸지 않는다.
        char a3 ='\u0061';//유니코드

        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
    }
}
